// Управляет показом/скрытием панели лидерборда.
//
// УСТАНОВКА НА СЦЕНЕ:
//   1. Прикрепи этот скрипт к объекту LeaderboardPanel
//   2. Назначь поля в Inspector:
//      - root          → сам LeaderboardPanel (GameObject)
//      - mainMenuPanel → панель главного меню (чтобы скрывать её)
//
// КНОПКИ:
//   - Кнопка "Рекорды" в меню → вызывает Show()
//   - Кнопка "Закрыть"        → вызывает Hide()
// ============================================================
using UnityEngine;

public class LeaderboardPanelUI : MonoBehaviour
{
    [Header("Панели")]
    [SerializeField] private GameObject root;
    // Сам LeaderboardPanel — показываем/скрываем

    [SerializeField] private GameObject mainMenuPanel;
    // Главное меню — скрываем когда открываем лидерборд
    // (по аналогии с PortfolioView)

    // --------------------------------------------------------
    // Вызывается при старте — панель скрыта по умолчанию.
    // --------------------------------------------------------
    private void Start()
    {
        Hide();
    }

    // --------------------------------------------------------
    // Показать панель лидерборда.
    // Вызови из кнопки "Рекорды" в главном меню.
    // --------------------------------------------------------
    public void Show()
    {
        // Скрываем главное меню
        if (mainMenuPanel != null)
            mainMenuPanel.SetActive(false);

        // Показываем панель лидерборда
        if (root != null)
            root.SetActive(true);
    }

    // --------------------------------------------------------
    // Скрыть панель лидерборда.
    // Вызови из кнопки "Закрыть".
    // --------------------------------------------------------
    public void Hide()
    {
        // Скрываем панель лидерборда
        if (root != null)
            root.SetActive(false);

        // Возвращаем главное меню
        if (mainMenuPanel != null)
            mainMenuPanel.SetActive(true);
    }
}
