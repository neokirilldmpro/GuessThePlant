// ============================================================
// ILeaderboardProvider.cs
// ------------------------------------------------------------
// ИНТЕРФЕЙС провайдера лидерборда.
//
// ЗАЧЕМ:
//   Всё взаимодействие с платформой (Яндекс / CrazyGames / др.)
//   идёт ТОЛЬКО через этот интерфейс. Игровая логика не знает,
//   на какой платформе она запущена.
//
// КАК ДОБАВИТЬ НОВУЮ ПЛАТФОРМУ:
//   Создай новый класс, реализуй этот интерфейс, зарегистрируй
//   его в LeaderboardService.CreateProvider().
// ============================================================
public interface ILeaderboardProvider
{
    // Доступен ли лидерборд на текущей платформе.
    // На Яндексе — true если игрок авторизован.
    // На CrazyGames — false (пока нет реализации).
    bool IsAvailable { get; }

    // Отправить результат в лидерборд.
    // leaderboardName — техническое название в консоли платформы.
    // score           — целое число (для времени передаём миллисекунды).
    void SubmitScore(string leaderboardName, int score);
}